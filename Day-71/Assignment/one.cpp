// Shallow Copy
// Copies only the reference (address) of the data
// Both objects share the same memory
//  If you change one object, the other also changes 
// “Two objects pointing to the same data”

// Deep Copy
// Creates a completely new copy of the data
// Each object has its own separate memory
// If you change one object, the other does NOT change 
// “A full independent duplicate is created”

// Key Difference
// Shallow Copy	                                      Deep Copy
// Same memory	                                      Separate memory
// Changes affect both	                              Changes independent
// Faster	                                          Slightly slower